#What is Scikit-Learn (Sklearn)
#Scikit-learn (Sklearn) is the most useful and robust library for machine learning in Python.
#It provides a selection of efficient tools for machine learning and statistical modeling including classification, regression, clustering
#and dimensionality reduction via a consistence interface in Python. This library, which is largely written in Python, is built upon NumPy, SciPy and Matplotlib

#pip install -U scikit-learn

#Supervised Learning algorithms − Almost all the popular
#supervised learning algorithms, like Linear Regression, Support Vector Machine (SVM), Decision Tree etc., are the part of scikit-learn.

#Unsupervised Learning algorithms − On the other hand, it also has all the popular unsupervised learning algorithms
#from clustering, factor analysis, PCA (Principal Component Analysis) to unsupervised neural networks.